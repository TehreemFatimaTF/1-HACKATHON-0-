def generate_reconciliation_report(results, output_path):
    """Generate reconciliation report in markdown."""
    md = f"""# Xero Reconciliation Report
**Generated:** {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}

---

## Summary

- **Matched Transactions:** {results['matched']}
- **Unmatched Xero Transactions:** {results['unmatched_xero']}
- **Unmatched Bank Transactions:** {results['unmatched_bank']}

## Status

"""
    
    if results['matched'] > 0 and results['unmatched_xero'] == 0 and results['unmatched_bank'] == 0:
        md += "✓ **All transactions reconciled successfully**\n"
    else:
        md += "⚠ **Discrepancies found - review required**\n"
    
    if results['discrepancies']:
        md += "\n## Discrepancies\n\n"
        for i, disc in enumerate(results['discrepancies'], 1):
            md += f"### {i}. {disc.get('description', 'Unknown')}\n"
            md += f"- **Date:** {disc.get('date', 'Unknown')}\n"
            md += f"- **Amount:** ${disc.get('amount', 0):.2f}\n"
            md += f"- **Issue:** {disc.get('issue', 'Unknown')}\n\n"
    
    md += "\n---\n\n*Generated by Financial Controller skill*\n"
    
    # Save report
    output_file = Path(output_path)
    output_file.parent.mkdir(parents=True, exist_ok=True)
    
    with open(output_file, 'w', encoding='utf-8') as f:
        f.write(md)
    
    print(f"✓ Reconciliation report saved to: {output_path}")


